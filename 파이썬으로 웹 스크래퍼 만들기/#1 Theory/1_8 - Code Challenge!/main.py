def plus(a, b):
    return float(a) + float(b)

def minus(a, b):
    return float(a) - float(b)

def times(a, b):
    return float(a) * float(b)

def division(a, b):
    return float(a) / float(b)

def remainder(a, b):
    return float(a) % float(b)

def negation(a):
    return -float(a)

def power(a, b):
    return float(a) ** float(b)

print(plus(18, 7))
print(minus(18, 7))
print(times(9, 7))
print(division(18, 6))
print(remainder(18, 7))
print(negation(18))
print(power(2, 7))